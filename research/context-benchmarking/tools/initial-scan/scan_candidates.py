def log(message: str) -> None:
    RESULTS.mkdir(parents=True, exist_ok=True)
    timestamp = dt.datetime.now(dt.timezone.utc).isoformat()
    line = f"[{timestamp}] {message}"
    print(line, flush=True)
    with LOG_PATH.open("a", encoding="utf-8") as handle:
        handle.write(line + "\n")


def run(
    args: list[str],
    *,
    cwd: Path | None = None,
    timeout: int = 180,
    check: bool = True,
) -> subprocess.CompletedProcess[str]:
    result = subprocess.run(
        args,
        cwd=str(cwd) if cwd else None,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        timeout=timeout,
        encoding="utf-8",
        errors="replace",
    )
    if check and result.returncode != 0:
        raise RuntimeError(
            f"command failed ({result.returncode}): {' '.join(args)}\n{result.stderr.strip()}"
        )
    return result


def verify_tools() -> None:
    for command in ("gh", "git"):
        if shutil.which(command) is None:
            raise RuntimeError(f"required command is unavailable: {command}")
    version = run(["gh", "--version"]).stdout.splitlines()[0]
    log(version)
    auth = run(["gh", "auth", "status"], check=False)
    if auth.returncode != 0:
        raise RuntimeError(f"gh is not authenticated:\n{auth.stdout}\n{auth.stderr}")
    account_line = next(
        (line.strip() for line in (auth.stdout + auth.stderr).splitlines() if "Logged in" in line),
        "gh authentication verified",
    )
    log(account_line)


def gh_json(args: list[str], timeout: int = 180) -> Any:
    result = run(["gh", *args], timeout=timeout)
    try:
        return json.loads(result.stdout)
    except json.JSONDecodeError as exc:
        raise RuntimeError(f"invalid JSON from gh {' '.join(args)}: {exc}") from exc


def search_repositories(max_candidates: int) -> list[dict[str, Any]]:
    # Several star bands reduce ranking bias and keep the pool within GitHub search limits.
    queries = [
        "language:Go stars:10..30 size:200..20000 pushed:>=2025-01-01 archived:false fork:false",
        "language:Go stars:31..100 size:200..20000 pushed:>=2025-01-01 archived:false fork:false",
        "language:Go stars:101..500 size:200..20000 pushed:>=2025-01-01 archived:false fork:false",
    ]
    by_name: dict[str, dict[str, Any]] = {}
    per_query = max(40, max_candidates // len(queries))
    for query in queries:
        log(f"searching repositories: {query}")
        data = gh_json(
            [
                "api",
                "-X",
                "GET",
                "search/repositories",
                "-f",
                f"q={query}",
                "-f",
                f"per_page={min(100, per_query)}",
                "-f",
                "sort=updated",
                "-f",
                "order=desc",
            ]
        )
        for item in data.get("items", []):
            by_name[item["full_name"]] = item
    items = list(by_name.values())
    items.sort(key=lambda item: (item.get("pushed_at", ""), item.get("stargazers_count", 0)), reverse=True)
    log(f"found {len(items)} unique repositories")
    return items[:max_candidates]


def metadata_to_analysis(item: dict[str, Any]) -> RepoAnalysis:
    license_data = item.get("license") or {}
    return RepoAnalysis(
        full_name=item["full_name"],
        url=item.get("html_url", ""),
        description=item.get("description") or "",
        stars=int(item.get("stargazers_count") or 0),
        size_kb=int(item.get("size") or 0),
        open_issues=int(item.get("open_issues_count") or 0),
        updated_at=item.get("updated_at") or "",
        pushed_at=item.get("pushed_at") or "",
        license=license_data.get("spdx_id") or "",
        default_branch=item.get("default_branch") or "",
        archived=bool(item.get("archived")),
        fork=bool(item.get("fork")),
    )


def safe_clone_name(full_name: str) -> str:
    digest = hashlib.sha1(full_name.encode("utf-8")).hexdigest()[:8]
    repo_name = full_name.rsplit("/", 1)[-1][:24]
    return f"{digest}_{repo_name}"


def clone_repo(repo: RepoAnalysis) -> Path:
    target = CLONES / safe_clone_name(repo.full_name)
    if target.exists():
        shutil.rmtree(target)
    CLONES.mkdir(parents=True, exist_ok=True)
    result = run(
        [
            "git",
            "-c",
            "core.longpaths=true",
            "clone",
            "--depth",
            "1",
            "--filter=blob:none",
            "--no-tags",
            repo.url,
            str(target),
        ],
        timeout=300,
        check=False,
    )
    if result.returncode != 0:
        repo.clone_error = result.stderr.strip()[-1000:]
        raise RuntimeError(repo.clone_error or "clone failed")
    repo.clone_ok = True
    repo.commit = run(
        ["git", "-c", "safe.directory=*", "rev-parse", "HEAD"],
        cwd=target,
    ).stdout.strip()
    return target


def iter_files(root: Path) -> Iterable[Path]:
    for current_root, dirnames, filenames in os.walk(root):
        dirnames[:] = [name for name in dirnames if name not in EXCLUDED_DIRS and not name.startswith(".")]
        current = Path(current_root)
        for filename in filenames:
            yield current / filename


def read_text(path: Path, max_bytes: int = 2_000_000) -> str:
    try:
        if path.stat().st_size > max_bytes:
            return ""
        return path.read_text(encoding="utf-8", errors="replace")
    except OSError:
        return ""


def is_generated_go(text: str) -> bool:
    header = "\n".join(text.splitlines()[:20]).lower()
    return any(marker in header for marker in GENERATED_MARKERS)


def analyze_clone(repo: RepoAnalysis, root: Path) -> None:
    workflow_dir = root / ".github" / "workflows"
    if workflow_dir.is_dir():
        repo.ci_files = sum(
            1
            for path in workflow_dir.iterdir()
            if path.is_file() and path.suffix.lower() in {".yml", ".yaml"}
        )

    packages: set[str] = set()
    architecture_docs: list[str] = []
    build_signals: set[str] = set()
    integration_terms = ("integration", "e2e", "end_to_end", "acceptance", "system_test")
    external_terms = (
        "stripe",
        "aws",
        "gcp",
        "azure",
        "oauth",
        "openid",
        "webhook",
        "grpc",
        "kafka",
        "nats",
        "redis",
        "smtp",
        "s3",
    )
    database_terms = ("database/sql", "gorm.io", "sqlx", "postgres", "mysql", "sqlite", "mongo")
    config_names = {
        ".env.example",
        "config.yaml",
        "config.yml",
        "config.json",
        "settings.yaml",
        "settings.yml",
        "docker-compose.yml",
        "docker-compose.yaml",
    }

    for path in iter_files(root):
        rel = path.relative_to(root)
        rel_lower = str(rel).replace("\\", "/").lower()
        name_lower = path.name.lower()

        if name_lower in {"makefile", "justfile", "taskfile.yml", "taskfile.yaml"}:
            build_signals.add(path.name)
        if name_lower == "go.mod":
            build_signals.add("go.mod")
        if name_lower in {"dockerfile", "compose.yml", "compose.yaml", "docker-compose.yml", "docker-compose.yaml"}:
            build_signals.add(path.name)
        if rel_lower.startswith(".github/workflows/") and path.suffix.lower() in {".yml", ".yaml"}:
            repo.ci_files += 1
        elif any(part in {".circleci", ".gitlab"} for part in rel.parts):
            repo.ci_files += 1

        if path.suffix.lower() in {".md", ".mdx"}:
            repo.markdown_files += 1
            if any(part.lower() in {"docs", "doc", "documentation"} for part in rel.parts[:-1]):
                repo.docs_markdown_files += 1
            if name_lower in ARCH_DOC_NAMES or "architecture" in name_lower or "design" in name_lower:
                architecture_docs.append(str(rel).replace("\\", "/"))
            if name_lower in {"readme.md", "readme.mdx"} and len(rel.parts) == 1:
                repo.readme_lines = len(read_text(path).splitlines())

        if name_lower in config_names or path.suffix.lower() in {".toml", ".ini"}:
            repo.config_files += 1

        if path.suffix.lower() != ".go":
            continue

        text = read_text(path)
        if not text:
            continue
        repo.go_files += 1
        lines = text.splitlines()
        line_count = len(lines)
        repo.largest_go_file_lines = max(repo.largest_go_file_lines, line_count)
        if line_count >= 500:
            repo.large_go_files += 1

        if is_generated_go(text):
            repo.generated_go_files += 1
            continue

        repo.loc += sum(1 for line in lines if line.strip() and not line.lstrip().startswith("//"))
        match = re.search(r"(?m)^package\s+([A-Za-z_][A-Za-z0-9_]*)", text)
        if match:
            packages.add(str(rel.parent).replace("\\", "/"))
        if name_lower.endswith("_test.go"):
            repo.test_files += 1
            if any(term in rel_lower for term in integration_terms) or "//go:build integration" in text:
                repo.integration_test_signals += 1

        text_lower = text.lower()
        if any(term in text_lower for term in database_terms):
            repo.database_signals += 1
        if any(term in text_lower for term in external_terms):
            repo.external_integration_signals += 1

    repo.package_count = len(packages)
    repo.architecture_docs = sorted(set(architecture_docs))[:20]
    repo.build_signals = sorted(build_signals)


def inspect_pull_requests(repo: RepoAnalysis, limit: int = 80) -> None:
    try:
        prs = gh_json(
            [
                "pr",
                "list",
                "--repo",
                repo.full_name,
                "--state",
                "merged",
                "--limit",
                str(limit),
                "--json",
                "number,title,body,mergedAt,additions,deletions,changedFiles,url",
            ],
            timeout=240,
        )
    except Exception as exc:  # noqa: BLE001 - preserve candidate scan on API failure
        repo.pr_error = str(exc)
        return

    repo.merged_prs_sampled = len(prs)
    cutoff = dt.datetime.now(dt.timezone.utc) - dt.timedelta(days=730)
    examples: list[dict[str, Any]] = []

    for pr in prs:
        body = pr.get("body") or ""
        title = pr.get("title") or ""
        additions = int(pr.get("additions") or 0)
        deletions = int(pr.get("deletions") or 0)
        changed_files = int(pr.get("changedFiles") or 0)
        total_delta = additions + deletions
        issue_refs = sorted(set(ISSUE_REF_RE.findall(body)))
        if issue_refs:
            repo.issue_linked_prs += 1
        if 2 <= changed_files <= 15 and 15 <= total_delta <= 800:
            repo.benchmark_sized_prs += 1
        if 4 <= changed_files <= 20:
            repo.cross_file_prs += 1
        merged_at = pr.get("mergedAt")
        if merged_at:
            try:
                when = dt.datetime.fromisoformat(merged_at.replace("Z", "+00:00"))
                if when >= cutoff:
                    repo.recent_prs += 1
            except ValueError:
                pass

        quality = 0
        quality += 3 if issue_refs else 0
        quality += 2 if 2 <= changed_files <= 15 else 0
        quality += 2 if 15 <= total_delta <= 800 else 0
        quality += 1 if re.search(r"(?i)\b(test|regression|bug|fix|refactor|feature)\b", title) else 0
        if quality >= 5:
            examples.append(
                {
                    "number": pr.get("number"),
                    "title": title,
                    "url": pr.get("url"),
                    "issue_refs": issue_refs,
                    "changed_files": changed_files,
                    "additions": additions,
                    "deletions": deletions,
                    "merged_at": merged_at,
                    "quality": quality,
                }
            )

    examples.sort(key=lambda item: (item["quality"], item.get("merged_at") or ""), reverse=True)
    repo.historical_task_examples = examples[:8]


def bounded(value: float, minimum: float = 0.0, maximum: float = 100.0) -> float:
    return max(minimum, min(maximum, value))


def score_repo(repo: RepoAnalysis) -> None:
    rejection: list[str] = []
    strengths: list[str] = []
    concerns: list[str] = []

    if not repo.clone_ok:
        rejection.append("clone failed")
    if repo.loc < 2_000:
        rejection.append("too little handwritten Go code")
    elif repo.loc > 80_000:
        rejection.append("too large for the initial benchmark")
    if repo.package_count < 4:
        rejection.append("too few package boundaries")
    if repo.test_files < 3:
        rejection.append("insufficient test surface")
    if repo.go_files and repo.generated_go_files / repo.go_files > 0.55:
        rejection.append("mostly generated Go code")
    if repo.license and repo.license not in PERMISSIVE_LICENSES:
        concerns.append(f"license requires review: {repo.license}")
    if not repo.license or repo.license == "NOASSERTION":
        concerns.append("license metadata missing or unclear")

    doc_gap = 0.0
    doc_gap += 28 if repo.docs_markdown_files == 0 else max(0, 20 - repo.docs_markdown_files * 2)
    doc_gap += 20 if not repo.architecture_docs else max(0, 12 - len(repo.architecture_docs) * 3)
    doc_gap += 16 if repo.readme_lines < 120 else 8 if repo.readme_lines < 250 else 0
    docs_per_package = repo.markdown_files / max(repo.package_count, 1)
    doc_gap += bounded(20 - docs_per_package * 5, 0, 20)
    doc_gap += 8 if repo.large_go_files >= 2 else 4 if repo.large_go_files == 1 else 0
    doc_gap += 8 if repo.external_integration_signals and repo.docs_markdown_files < 3 else 0
    repo.documentation_gap_score = round(bounded(doc_gap), 1)

    structure = 0.0
    structure += bounded(repo.package_count * 3, 0, 35)
    structure += bounded(repo.loc / 1000 * 2, 0, 25)
    structure += min(12, repo.config_files * 2)
    structure += 10 if repo.database_signals else 0
    structure += 10 if repo.external_integration_signals else 0
    structure += min(8, repo.large_go_files * 2)
    repo.structure_score = round(bounded(structure), 1)

    testability = 0.0
    testability += bounded(repo.test_files * 3, 0, 50)
    testability += 18 if repo.ci_files else 0
    testability += 12 if "go.mod" in repo.build_signals else 0
    testability += 10 if any(name.lower().startswith(("makefile", "taskfile", "justfile")) for name in repo.build_signals) else 0
    testability += min(10, repo.integration_test_signals * 3)
    repo.testability_score = round(bounded(testability), 1)

    history = 0.0
    history += bounded(repo.merged_prs_sampled / 80 * 20, 0, 20)
    history += bounded(repo.issue_linked_prs * 3, 0, 30)
    history += bounded(repo.benchmark_sized_prs * 2, 0, 25)
    history += bounded(repo.cross_file_prs * 1.5, 0, 15)
    history += bounded(repo.recent_prs, 0, 10)
    repo.history_score = round(bounded(history), 1)

    operational = 0.0
    operational += 35 if repo.license in PERMISSIVE_LICENSES else 10 if repo.license else 0
    operational += 25 if repo.size_kb <= 20_000 else 10
    operational += 20 if repo.ci_files else 0
    operational += 20 if repo.pushed_at >= "2025-01-01" else 0
    repo.operational_score = round(bounded(operational), 1)

    weights = {
        "documentation_gap": 0.26,
        "structure": 0.22,
        "testability": 0.22,
        "history": 0.24,
        "operational": 0.06,
    }
    total = (
        repo.documentation_gap_score * weights["documentation_gap"]
        + repo.structure_score * weights["structure"]
        + repo.testability_score * weights["testability"]
        + repo.history_score * weights["history"]
        + repo.operational_score * weights["operational"]
    )
    if rejection:
        total -= 35
    repo.total_score = round(bounded(total), 1)

    if repo.documentation_gap_score >= 65:
        strengths.append("large documentation gap")
    if repo.structure_score >= 55:
        strengths.append("meaningful cross-package structure")
    if repo.testability_score >= 60:
        strengths.append("strong automated-test surface")
    if repo.history_score >= 55:
        strengths.append("good historical benchmark-task inventory")
    if repo.issue_linked_prs >= 5:
        strengths.append("multiple issue-linked merged PRs")
    if repo.external_integration_signals:
        strengths.append("external integration boundaries")
    if repo.database_signals:
        strengths.append("database or persistence boundary")

    if repo.docs_markdown_files >= 20 or len(repo.architecture_docs) >= 5:
        concerns.append("may already be too well documented")
    if repo.merged_prs_sampled < 10:
        concerns.append("thin merged-PR history")
    if repo.issue_linked_prs == 0:
        concerns.append("no issue-linked PRs found in sample")
    if repo.pr_error:
        concerns.append("PR history inspection failed")
    if repo.largest_go_file_lines >= 1500:
        concerns.append("contains very large source files")

    repo.rejection_reasons = rejection
    repo.strengths = strengths
    repo.concerns = concerns


def analyze_one(repo: RepoAnalysis) -> RepoAnalysis:
    try:
        log(f"cloning {repo.full_name}")
        root = clone_repo(repo)
        analyze_clone(repo, root)
    except Exception as exc:  # noqa: BLE001 - one bad repo must not abort the scan
        repo.clone_error = str(exc)
        log(f"clone/analyze failed for {repo.full_name}: {exc}")
    return repo


def preliminary_score(repo: RepoAnalysis) -> float:
    score = 0.0
    score += 25 if 500 <= repo.size_kb <= 12_000 else 12 if repo.size_kb <= 20_000 else 0
    score += 20 if 5 <= repo.open_issues <= 80 else 10 if repo.open_issues else 0
    score += 20 if repo.license in PERMISSIVE_LICENSES else 5
    score += 20 if 15 <= repo.stars <= 150 else 12 if repo.stars <= 300 else 5
    score += 15 if repo.pushed_at >= "2025-07-01" else 8
    return score


def write_outputs(repos: list[RepoAnalysis], args: argparse.Namespace) -> None:
    RESULTS.mkdir(parents=True, exist_ok=True)
    ordered = sorted(repos, key=lambda repo: repo.total_score, reverse=True)
    payload = {
        "generated_at": dt.datetime.now(dt.timezone.utc).isoformat(),
        "configuration": vars(args),
        "scoring_note": "Scores rank benchmark suitability; they are not software-quality judgments.",
        "repositories": [asdict(repo) for repo in ordered],
    }
    (RESULTS / "candidates.json").write_text(json.dumps(payload, indent=2), encoding="utf-8")

    accepted = [repo for repo in ordered if not repo.rejection_reasons]
    shortlist = accepted[: args.shortlist]
    lines = [
        "# Demon Docs Context-Injection Benchmark Shortlist",
        "",
        f"Generated: {payload['generated_at']}",
        "",
        "> These scores estimate benchmark usefulness, not repository or maintainer quality. ‘Documentation gap’ means the repository may expose useful context-retrieval challenges.",
        "",
        "## Recommended candidates",
        "",
    ]
    if not shortlist:
        lines.append("No repository passed the mechanical minimums. Review `candidates.json` and relax thresholds deliberately.")
    for index, repo in enumerate(shortlist, start=1):
        lines.extend(
            [
                f"### {index}. [{repo.full_name}]({repo.url}) — {repo.total_score}/100",
                "",
                repo.description or "No repository description.",
                "",
                f"- Snapshot: `{repo.commit}`",
                f"- Handwritten Go LOC: {repo.loc:,}; packages: {repo.package_count}; Go files: {repo.go_files}; test files: {repo.test_files}",
                f"- Docs: {repo.markdown_files} Markdown files; {repo.docs_markdown_files} under docs; README: {repo.readme_lines} lines; architecture docs: {len(repo.architecture_docs)}",
                f"- History sampled: {repo.merged_prs_sampled} merged PRs; issue-linked: {repo.issue_linked_prs}; benchmark-sized: {repo.benchmark_sized_prs}",
                f"- Scores: documentation gap {repo.documentation_gap_score}; structure {repo.structure_score}; testability {repo.testability_score}; history {repo.history_score}; operations {repo.operational_score}",
                f"- Strengths: {', '.join(repo.strengths) if repo.strengths else 'none identified mechanically'}",
                f"- Concerns: {', '.join(repo.concerns) if repo.concerns else 'none identified mechanically'}",
                "",
                "Historical task examples:",
                "",
            ]
        )
        if repo.historical_task_examples:
            for example in repo.historical_task_examples[:5]:
                refs = ", ".join(f"#{ref}" for ref in example["issue_refs"]) or "none"
                lines.append(
                    f"- [PR #{example['number']}]({example['url']}) — {example['title']} "
                    f"({example['changed_files']} files, +{example['additions']}/-{example['deletions']}, issue refs: {refs})"
                )
        else:
            lines.append("- No high-confidence examples found in the sampled PR metadata.")
        lines.append("")

    lines.extend(
        [
            "## Selection procedure",
            "",
            "Before adopting a repository, manually validate its top historical tasks:",
            "",
            "1. Check out the parent commit of each accepted PR.",
            "2. Confirm the repository builds and its baseline tests pass at that commit.",
            "3. Recover the original issue text without reading the accepted patch.",
            "4. Confirm the task requires repository discovery rather than isolated syntax work.",
            "5. Confirm demon-docs can select useful context without embedding the expected implementation.",
            "6. Pin the base commit, issue text, verification command, and accepted PR in the benchmark manifest.",
            "",
            "## Rejected candidates",
            "",
        ]
    )
    for repo in ordered:
        if repo.rejection_reasons:
            lines.append(
                f"- [{repo.full_name}]({repo.url}) — {repo.total_score}/100: {', '.join(repo.rejection_reasons)}"
            )
    (RESULTS / "shortlist.md").write_text("\n".join(lines) + "\n", encoding="utf-8")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    parser.add_argument("--search-limit", type=int, default=180, help="maximum metadata candidates")
    parser.add_argument("--clone-limit", type=int, default=30, help="maximum repositories to clone")
    parser.add_argument("--history-limit", type=int, default=15, help="maximum repositories to inspect for PR history")
    parser.add_argument("--shortlist", type=int, default=10, help="number of accepted repositories in the report")
    parser.add_argument("--workers", type=int, default=4, help="parallel shallow-clone workers")
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    RESULTS.mkdir(parents=True, exist_ok=True)
    LOG_PATH.write_text("", encoding="utf-8")
    verify_tools()

    raw = search_repositories(args.search_limit)
    repos = [metadata_to_analysis(item) for item in raw]
    repos.sort(key=preliminary_score, reverse=True)
    clone_targets = repos[: args.clone_limit]
    log(f"cloning/analyzing {len(clone_targets)} repositories with {args.workers} workers")

    analyzed: list[RepoAnalysis] = []
    with concurrent.futures.ThreadPoolExecutor(max_workers=args.workers) as executor:
        futures = {executor.submit(analyze_one, repo): repo for repo in clone_targets}
        for future in concurrent.futures.as_completed(futures):
            analyzed.append(future.result())

    # Rank static-analysis results before spending API calls on PR history.
    for repo in analyzed:
        score_repo(repo)
    analyzed.sort(
        key=lambda repo: (
            repo.documentation_gap_score * 0.35
            + repo.structure_score * 0.30
            + repo.testability_score * 0.35
        ),
        reverse=True,
    )

    history_targets = analyzed[: args.history_limit]
    log(f"inspecting merged PR history for {len(history_targets)} repositories")
    for repo in history_targets:
        log(f"PR history: {repo.full_name}")
        inspect_pull_requests(repo)
        score_repo(repo)

    # Preserve cloned candidates that were not history-enriched with their static scores.
    for repo in analyzed[args.history_limit :]:
        score_repo(repo)

    write_outputs(analyzed, args)
    accepted = [repo for repo in analyzed if not repo.rejection_reasons]
    accepted.sort(key=lambda repo: repo.total_score, reverse=True)
    log(f"scan complete: {len(accepted)} mechanically accepted candidates")
    for index, repo in enumerate(accepted[: args.shortlist], start=1):
        log(f"{index}. {repo.full_name}: {repo.total_score}")
    return 0


